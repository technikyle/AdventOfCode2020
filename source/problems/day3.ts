type MapType = ('#'|'.'|'0')[][]

function parseLine(x: string): ('#'|'.'|'0')[] {
    return [...x] as ('#'|'.'|'0')[]
}

let lines: MapType = input.split('\n').filter(x => x != '').map((line) => {
    let chars = parseLine(line)
    return chars
})

function enumerate(advance: (x: number, y: number) => [number, number], work: (x: number, y: number) => void) {
    var y = 0
    var x = 0

    while (y < lines.length) {
        work(x, y)
        let [nextX, nextY] = advance(x,y)
        x = nextX % lines[0].length
        y = nextY
    }
}

function check(combination: [number, number]): number {
    let advance: (x: number, y: number) => [number, number] = (x, y) => [x + combination[0], y + combination[1]] 

    var result = 0

    let count: (x: number, y: number) => void = (x, y) => {
        let value = lines[y][x]
        if (value == "#") {
            result += 1
        }
    }

    enumerate(advance, count)

    return result
}

export function p1(): number {

    let result = check([3, 1])

    return result
    // return result as number
}

export function p2(): number {

    function checkAll(combinations: [number, number][]): number {
        var overallResult = 1
        combinations.forEach(element => {
            overallResult *= check(element)
        });
        return overallResult
    }

    let combinations: [number, number][] = [
        [1,1], [3, 1], [5, 1], [7,1], [1,2]
    ]

    let result = checkAll(combinations)

    return result
    // return result as number
}